import { Outlet, useLoaderData } from '@remix-run/react';
import type { LoaderFunctionArgs, LinksFunction } from '@vercel/remix';
import { json, redirect } from '@vercel/remix';

import tiptapStyles from '~/assets/styles/tiptap.css?url';
import TiptapList from '~/components/tiptap/list';
import { getDocumentList } from '~/.server/tiptap/api';
import { commitSession, getSession } from '~/session';

export const links: LinksFunction = () => [
  {
    rel: 'stylesheet',
    href: tiptapStyles,
  },
];

export const loader = async ({ request }: LoaderFunctionArgs) => {
  const session = await getSession(request.headers.get('Cookie'));

  if (!session.has(process.env.TIPTAP_TOKEN_KEY)) {
    return redirect('/login');
  }

  const documentList = await getDocumentList();
  return json(documentList, {
    headers: {
      'Set-Cookie': await commitSession(session),
    },
  });
};

const EditorIndex: React.FC = () => {
  const docList = useLoaderData<typeof loader>();

  return (
    <div className="flex w-screen h-screen">
      <div className="w-64 p-6 border-r-2 border-gray-300">
        <TiptapList docList={docList} />
      </div>
      <div className="flex-1">
        <Outlet />
      </div>
    </div>
  );
};

export default EditorIndex;

